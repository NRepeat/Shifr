homophones = {
    'A': ['12', '25'], 'B': ['14', '39'], 'C': ['17', '26'], 'D': ['18', '27'], 'E': ['01', '50'],
    'F': ['33', '19'], 'G': ['05', '40'], 'H': ['36', '42'], 'I': ['07', '21'], 'K': ['02', '37'],
    'L': ['29', '48'], 'M': ['23', '16'], 'N': ['20', '49'], 'O': ['03', '45'], 'P': ['13', '38'],
    'R': ['31', '41'], 'S': ['09', '30'], 'T': ['10', '34'], 'U': ['24', '46'], 'V': ['11', '44']
}

def encrypt(text):
    import random
    return ''.join(random.choice(homophones[char.upper()]) for char in text if char.upper() in homophones)

